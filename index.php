<p>This cemetery is located on the north side of Congress Avenue on the west side of the City of Rochester, N. Y.</p>
<p>This cemetery was probably founded between 1810 and 1812. The property was originally owned by the Wadsworth family which owned land from Geneseo to Rochester. The Wadsworths set aside one and a quarter acre for a burial place of area residents. The cemetery resided in the Town of Gates until 1902 when the area was annexed into the City of Rochester. The road leading to the cemetery was originally called Cemetery Road. Then between 1880 and 1890 the name was changed to Chester Street. In 1899, Chester Street became Congress Avenue.</p>
<p>On 20 September 1883 Herbert and William Wadsworth sold the cemetery lot to the newly formed "Rapids Cemetery Association." Monroe H. Oakley was the first president of the organization and Isaac Loomis was treasurer. The last known record of the Rapids Cemetery Association was in 1900 when they paid property taxes. The map, below, was traced from one probably made in the 1890s, although the exact date is unknown. It also included a list of plot owner, which relate to the map.</p>
<p>Almost all the tombstones have been knocked over. The listing of tombstone inscriptions below was made from old transcriptions and a visit to the cemetery in the late 1980s.</p>
